let codeArea = document.getElementById("codeArea");
let drawArea = document.getElementById("drawArea");
let drawAreaCtx = drawArea.getContext("2d");
let splitCode = "";
let pixels = [];
let currentColor = "0x07";
let colorSelected = document.getElementById("currentColor");

let isMagicWanding = false;


function Awake() {
    codeArea.value = blankImage;
    splitCode = codeArea.value.split("\n");

    let canvasSize = 512;

    drawArea.width = canvasSize;
    drawArea.height = canvasSize;

    drawAreaCtx.imageSmoothingEnabled = false;
    drawAreaCtx.webkitImageSmoothingEnabled = false;
    drawAreaCtx.mozImageSmoothingEnabled = false;
    drawAreaCtx.msImageSmoothingEnabled = false;
    drawAreaCtx.oImageSmoothingEnabled = false;

    let canvasScale = canvasSize / 32;
    drawAreaCtx.scale(canvasScale, canvasScale);

    let code = codeArea.value;
    let lines = code.split("\n");

    for (let i = 0; i < lines.length; i++) {
        let line = lines[i];
        let bytes = line.split(", ");

        for (let j = 0; j < bytes.length; j++) {
            let byte = bytes[j];
            if(byte.charAt(0) == "."){
                byte = byte.substring(4, byte.length);
            }
            let pixel = new Pixel(j, i, byte);
            pixels.push(pixel);
        }
    }

    for (let i = 0; i < pixels.length; i++) {
        let pixel = pixels[i];
        pixel.draw();
    }


    drawArea.addEventListener("click", mouseClick);
    drawArea.addEventListener("mousemove", mouseMove);
}

function UpdateCurrentColor(color) {
    currentColor = color;
    colorSelected.style.backgroundColor = getRealColor(color);
}

function SetMagicWand(){
    isMagicWanding = !isMagicWanding;

    if(isMagicWanding){
        document.getElementById("magicWandButton").style.backgroundColor = "green";
    }else{
        document.getElementById("magicWandButton").style.backgroundColor = "white";
    }
}

function UpdatedCode() {
    let newCode = codeArea.value;
    let newLines = newCode.split("\n");
    if(newLines.length != 32){
        DrawErrorImageInfoInCanvas();
        return;
    }

    for (let i = 0; i < newLines.length; i++) {
        let newLine = newLines[i];
        let newBytes = newLine.split(", ");
        if(newBytes.length != 32){
            DrawErrorImageInfoInCanvas();
            return;
        }

        for (let j = 0; j < newBytes.length; j++) {
            let newByte = newBytes[j];
            if(newByte.charAt(0) == "."){
                newByte = newByte.substring(4, newByte.length);
            }
            let pixel = pixels[i * 32 + j];
            if (pixel) {
                pixel.ChangeColor(newByte);
                pixel.draw();
            }
        }
    }
}

