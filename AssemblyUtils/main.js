function magicWand(x, y, color){
    let row = y;
    let column = x;
    let targetColor = pixels[row * 32 + column].color;

    if(targetColor == color){
        return;
    }

    let queue = [];
    queue.push([row, column]);

    while(queue.length > 0){
        let node = queue.pop();
        let row = node[0];
        let column = node[1];

        if(row < 0 || row > 31 || column < 0 || column > 31){
            continue;
        }

        if(pixels[row * 32 + column].color != targetColor){
            continue;
        }

        pixels[row * 32 + column].ChangeColor(color);
        pixels[row * 32 + column].draw();
        splitCode[row] = splitCode[row].substring(0, column * 6 + 4) + color + splitCode[row].substring(column * 6 + 8, splitCode[row].length);

        queue.push([row + 1, column]);
        queue.push([row - 1, column]);
        queue.push([row, column + 1]);
        queue.push([row, column - 1]);
    }

    codeArea.value = splitCode.join("\n");
}

