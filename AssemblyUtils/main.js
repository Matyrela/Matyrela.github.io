function mouseClick(event){
    if(isMagicWanding){
        let x = Math.floor(event.offsetX / 16);
        let y = Math.floor(event.offsetY / 16);

        if(x < 0 || x > 31 || y < 0 || y > 31){
            return;
        }

        let row = y;
        let column = x;

        magicWand(column, row, currentColor);
        SetMagicWand();
    }else{
        UpdatePixel(event);
    }

    console.log(isMagicWanding);
}

function mouseMove(event){
    if(event.buttons == 1){
        UpdatePixel(event);
    }
}

function UpdatePixel(event){
    let x = Math.floor(event.offsetX / 16);
    let y = Math.floor(event.offsetY / 16);

    if(x < 0 || x > 31 || y < 0 || y > 31){
        return;
    }

    let row = y;
    let column = x;

    pixels[row * 32 + column].ChangeColor(currentColor);
    pixels[row * 32 + column].draw();

    splitCode[row] = splitCode[row].substring(0, column * 6 + 4) + currentColor + splitCode[row].substring(column * 6 + 8, splitCode[row].length);
    codeArea.value = splitCode.join("\n");
}

function DrawErrorImageInfoInCanvas(){
    drawAreaCtx.clearRect(0, 0, drawArea.width, drawArea.height);
    drawAreaCtx.fillStyle = "red";
    drawAreaCtx.font = "1px Arial";
    drawAreaCtx.fillText("Error: La imagen debe de ser de 32x32 px", 0, 1);
}

function getRealColor(color){
        switch(color){
            case "0x07":
                return "white";

            case "0x06":
                return "purple";

            case "0x05":
                return "cyan";

            case "0x04":
                return "blue";

            case "0x03":
                return "yellow";

            case "0x02":
                return "red";

            case "0x01":
                return "green";

            case "0x00":
                return "black";

            default:
                return "brown"
        }
}

function Pixel(x, y, color) {
    this.x = x;
    this.y = y;
    this.color = color;

    this.draw = function() {
        drawAreaCtx.fillStyle = getRealColor(this.color);
        drawAreaCtx.fillRect(this.x, this.y, 1, 1);
    }

    this.ChangeColor = function(color) {
        this.color = color;
    }
}
